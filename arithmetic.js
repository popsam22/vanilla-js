/*Arithmetic operations in JS
+ addition
- subtraction
* multiplication
/ division
*/

console.log(5 + 5);
console.log(1 + 2 + 3 + 4 + 7 + 8 + 9);
console.log(17 - 89);
console.log(0 * 9);
console.log(2**5);
console.log(8 / 2);
console.log(9 % 2);

/*Comparison operations in JS which return boolean value
> greater than
< less than
== equal to
!= not equal to
*/
console.log(15 > 7);
console.log(3 < 2);
console.log( 44 == 44);
console.log(777 != 888);


//strict equality (===) compares both the value and data type to return a boolean
console.log(14 == '14');
console.log(14 === '14');

//strict unequality (!==) compares both value and data type to return a boolean
console.log(10 != 10);
console.log(10 != '10');
console.log(10 !== '10');